<p>The sky darkens in the minutes before a summer-afternoon downpour.  The
slower arrival of fronts is signalled by a slower darkening.  Such facts
explain why, despite modern weather forecasts, people still use sky brightness
as a predictor of local weather.  All it takes is a little communication to
leverage this into forecasts of regional weather.  For example, a storm moving
up the coast of Nova Scotia will darken skies in Yarmouth a few hours before
bad weather gets to Halifax, and so a few well-placed phone calls could improve
the range of forecasts for Halifax.</p>

<p>But not everyone can spend the day watching the sky and chatting on the
phone.  The solution is to set up a network of automated light sensors.  This
could be done in a professional way, at professional costs.  The SkyNet program
proposes another solution: do it all for essentially no costs.  Put the sensors
in the schools.  This will yield good spatial coverage, because all populated
areas have schools.  It will also cost almost nothing, because the students
will be doing the work of constructing, installing, and maintaining the light
sensors.  Far from an exercise in modern-day child labour, SkyNet is actually a
thinly-disguised learning experience.  The experience could be very broad, indeed.
Science-minded students would benefit from observing the atmosphere, and
thinking about the Physics involved.  Engineering-minded students would benefit
from setting up and calibrating the equipment.  There are obvious opportunities for
students interested in statistics and computing.  Business-minded students
could organize the work, mind the finances, deal with local sponsors, etc.
Good writers will be needed to communicate with the public.  Students
interested in history will find that SkyNet is using principles that tie
directly to generations-old methods of celestial navigation.  The list goes on;
the point is that SkyNet could offer much, to many students.</p>

<p>There project need not be limited to the measurement of light.  With modern
micro-controllers and sensors, students could measure many aspects of weather
that were once the domain of the professionals.</p>

<p>It seems likely that people would start to consult SkyNet for informal
short-term local weather forecasts.  Can anyone doubt the excitement, and
pride, that students would feel when that happens?</p>

